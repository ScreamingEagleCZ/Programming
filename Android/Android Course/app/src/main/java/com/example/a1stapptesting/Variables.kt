package com.example.a1stapptesting

fun main(args: Array<String>) {
    var a = 10  // Mutable variable
    var b = 20
    var c = a + b
    val d = 30  // Inmutable variable
    var power : Int = 200
    var x:Boolean = true
    var y:Char = 'R'
    var z:Byte = 12
    var o:Short = -356
    var p:Int = 43543
    var q:Long = -457812548L    // Notice L at end
    var r:Float = 5.7458745F    // Notice F at end
    var s:Double = 7.4567896445
    println(c)
    println(d)
    println(power)
    println(x)
    println(y)
    println(z)
    println(o)
    println(p)
    println(q)
    println(r)
    println(s)
}