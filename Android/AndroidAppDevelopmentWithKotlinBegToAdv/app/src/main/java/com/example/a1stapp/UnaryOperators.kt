package com.example.a1stapp

fun main() {
    var number:Double = 7.6
    var isCheck:Boolean = true

    println("Number = " + +number)
    println("Number = " + -number)
    println("Number = " + ++number)
    println("Number = " + --number)
    println("Number = " + !isCheck)

    println("-----------------------")

    var result:Double = 4.7

    println("Result = " + result++)
    println("Result = " + result)
    println("Result = " + ++result)
    println("Result = " + result)
}